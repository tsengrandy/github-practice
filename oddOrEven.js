// Update the following function to take an argument and output 'Even' if the argument is an even number or 'Odd' if the number is an odd number.

function oddOrEven(numb){
    if (numb%2 === 0){
        console.log("Even")
    } else {
        console.log("Odd");
    }
}

oddOrEven(4)
oddOrEven(11)
oddOrEven(75)
